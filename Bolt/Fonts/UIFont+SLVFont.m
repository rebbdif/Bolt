#import "UIFont+SLVFont.h"

@implementation UIFont (SLVFont)

+ (UIFont *)titleHelvetica {
    return [UIFont fontWithName:@"Helvetica-Bold" size:36];
}

+ (UIFont *)lightHelveticaLabels {
    return [UIFont fontWithName:@"Helvetica-Light" size:20];
}

+ (UIFont *)boldHelvetica {
    return [UIFont fontWithName:@"Helvetica-Bold" size:18];
}

@end
